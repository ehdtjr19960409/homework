package Section02;

public class Ex02_17 {
	public static void main(String[] args) {

		int alphabetA = 'A'; // 정수 타입 변수 선언 및 초기화(유니코드로 변환)
		int alphabetB = 'B'; // 정수 타입 변수 선언 및 초기화(유니코드로 변환)

		System.out.println(alphabetA); // 변수안에 유니코드로 변경된 정수타입 출력
		System.out.println(alphabetB); // 변수안에 유니코드로 변경된 정수타입 출력

		char alphabetC = 67; // 숫자 67를 유니코드로 변경해서 문자타입 변수에 선언 및 초기화
		System.out.println(alphabetC); // 변수안에 있는 문자 출력

	}
}
