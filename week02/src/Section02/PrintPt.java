package Section02;

public class PrintPt {
public static void main(String[] args) {
	System.out.printf("%.1f",1234567); //소수점 첫번째 자리까지 출력
	System.out.println();		//줄바꿈
	System.out.printf("%.2f",1234567);//소수점 두번째 자리까지 출력
	System.out.println();		//줄바꿈
	System.out.printf("%.3f",1234567);//소수점 세번째 자리까지 출력
	System.out.println();		//줄바꿈
	System.out.printf("%.4f",1234567);//소수점 네번째 자리까지 출력
	System.out.println();		//줄바꿈
	System.out.printf("%.5f",1234567);//소수점 다섯번째 자리까지 출력
	System.out.println();		//줄바꿈
}
}
