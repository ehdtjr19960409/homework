package Section02;

public class Ex02_14 {
public static void main(String[] args) {
	double pieDouble = 3.141592653589793; //더블형타입에 변수 선언 및 초기화
	float pieFloat = 3.141592653589793f;  //실수형 타입에 변수 선언및 초기화
	
	System.out.println("double : " + pieDouble); //pieDouble은 해당 double형 범위만큼 값을 출력
	System.out.println("float : " + pieFloat);//pieFloat은 해당 float형 범위만큼 값을 출력
}
}
