package Section02;

public class Ex02_12 {
	public static void main(String[] args) {
		
		byte num1 = 0110; //btye형 변수에 0110 값 저장 및 초기화
		short num2 = 0107; //short형 변수에 0107 값 저장 및 초기화
		int num3 = 0x46; //int형 0x46값 저장 및 초기화
		long num4 = 69L; // long형 69L 값 저장 및 초기화
		
		
		System.out.println(num1); //정수형으로 값 출력
		System.out.println(num2); //정수형으로 값 출력
		System.out.println(num3); //정수형으로 값 출력
		System.out.println(num4); //값에 리터럴 표시가 있으므로 정수형으로 값 출력
		
	}
	
}
