package Section02;

public class Ex02_19 {
public static void main(String[] args) {
	boolean isStudent = true; // 논리형타입으로 변수 선언 및 초기화
	
	if(isStudent) { //조건식에서 변수가 참이라면 실행
		System.out.println("저는 학생입니다."); //참인 출력메소드 출력
	}else { //참이 아니라면 실행
		System.out.println("저는 학생이 아닙니다."); //거짓인 출력메소드 출력
	}
}
}
