package Section03;

public class Ex03_13 {
	public static void main(String[] args) {
		int a= 10; //정수형변수 선언
		int b =20; //정수형 변수 선언
		
		System.out.println(a>b); //a와 b 중 a가 크면 참 출력
		System.out.println(a<=b); //a와 b 중 b와 같거나 크면 참 출력
		System.out.println(a==b); //a와 b 같을떄 참으로 출력
		System.out.println(a != b); //a와 b가 같지 않을 때 참으로 출력
		
		
	}
}
