package Section03;

public class Ex03_14 {
	public static void main(String[] args) {
		int a= 10; //정수형변수 선언
		int b =11; //정수형 변수 선언
		
		System.out.println(a<=b); //a와 b 중 a가 크면 참 출력
		System.out.println(a==b); //a와 b 중 b와 같거나 크면 참 출력
		System.out.println(a != --b); // a와 증감 --한 값 b가 같지 않으면 참 출력
	}
}
