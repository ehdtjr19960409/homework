package Section03;

public class Ex03_01 {
	public static void main(String[] args) {
		int x = 100; //정수형 변수 선언
		int resultPlus = +x; // 정수형으로 선언된 resultPlus 값에 x를 더해 resultPlus에 저장한다
		int resultMinus = -x; // 정수형으로 선언된 resultMinus 값에 x를 빼 resultMinus에 저장한다
		System.out.println(resultPlus); //resultPlus 변수 값 출력
		System.out.println(resultMinus); //resultMinus 변수 값 출력
		
		
	}
}
