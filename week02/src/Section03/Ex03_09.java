package Section03;

public class Ex03_09 {
	public static void main(String[] args) {
		
		int x = 100; //정수변수 선언
		int y = 200; //정수변수 선언
		
		System.out.println(x+y); //더하기 연산후 출력
		System.out.println(x-y); //뺴기 연산 후 출력
		System.out.println(x*y); // 곱하기 연산 후 출력
		System.out.println(x/y); // 나누기 연산 후 몫 출력
		System.out.println(x%y); // 나누기 연산 후 나머지 출력
		
		
	}
}
